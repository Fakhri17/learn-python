#Operator Arimatika
# Terdiri dari: 
# Penjumlahan (+), Pengurangan (-), Perkailian (*)
# Pembagian (/), Sisa Bagi (%), Pangkat (**), Pembagian Bulat (//)

#(+)
print (20 + 22)
harga1 = 15
harga2 = 20
total = harga1 + harga2
print (total)	

#(-)
hutang = 100000
bayar = 50000
sisaHutang = hutang - bayar
print (sisaHutang)

#(*)
panjang = 10
lebar = 5
luas = panjang * lebar
print(luas)

#(/)
uang = 50000
anak = 5
uangSaku = uang / anak
print (" Uang saku tiap anak adalah ", int(uangSaku))


#(%)
bilangan1 = 14
bilangan2 = 5
hasil = bilangan1 % bilangan2
print("Sisa bagi dari bilangan ", bilangan1, " dan ", bilangan2, " adalah ", hasil)

#(**)
bilangan3 = 8
bilangan4 = 2
hasilPangkat = bilangan3 ** bilangan4
print(hasilPangkat)

#Pembagian Bulat
print(10//3) 

#Perbandingan
print(1 == 1) # bernilai True Jika masing-masing operan memiliki nilai yang sama, maka kondisi bernilai benar atau True.
print(1 != 1) # bernilai False Akan menghasilkan nilai kebalikan dari kondisi sebenarnya.